#include "ft_printf.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	size_t	size_of_s1;
	char	*copy;
	size_t	i;

	i = 0;
	size_of_s1 = ft_strlen(s1);
	copy = (char *)malloc(size_of_s1 + 1);
	if (!copy)
		return (NULL);
	while (s1[i])
	{
		copy[i] = s1[i];
		i++;
	}
	copy[i] = 0;
	return (copy);
}
